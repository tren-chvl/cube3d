#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ret;
	size_t	i;

	i = 0;
	ret = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (ret == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		ret[i] = s[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
